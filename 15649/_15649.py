N, M = map(int, input().split())
ary = []

def dfs():
    if len(ary) == M:
        print(' '.join(map(str, ary)))
        return
    
    for i in range(N):
        if i+1 not in ary:
            ary.append(i+1)
            dfs()
            ary.pop()
dfs()